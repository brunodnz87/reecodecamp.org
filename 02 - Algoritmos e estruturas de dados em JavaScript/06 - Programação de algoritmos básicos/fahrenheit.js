
function convertCtoF(celsius) {
    let fahrenheit;
    return fahrenheit = celsius*9/5+32;
  }

console.log("Conversão para Fahrenheit 30° Celsius = " + convertCtoF(30) + "° Fahrenheit");
console.log("Conversão para Fahrenheit 0° Celsius = " + convertCtoF(0) + "° Fahrenheit");
console.log("Conversão para Fahrenheit -30° Celsius = " + convertCtoF(-30) + "° Fahrenheit");
console.log("Conversão para Fahrenheit -10° Celsius = " + convertCtoF(-10) + "° Fahrenheit");
console.log("Conversão para Fahrenheit 20° Celsius = " + convertCtoF(20) + "° Fahrenheit");